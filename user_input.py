user_input = input("enter a character: ")[0]  # input() allows user to enter input
user_input2 = input("enter a second character: ")[0]  # [0] means we only take the first character from the users input
manipulate_input = user_input + user_input2  # concatenate user input
print(manipulate_input)  # print user input

